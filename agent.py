def match_jobs(user_skills, jobs):
    matched = []

    for job in jobs:
        score = len(set(user_skills) & set(job["skills"]))

        if score > 0:
            job["match_score"] = score
            matched.append(job)

    return sorted(matched, key=lambda x: x["match_score"], reverse=True)

